public class CommentsExample {

	/*#main함수(메서드)는 프로그램 실행 시 자동으로 
	호출되는 함수이며, 실행에 반드시 필요한 함수입니다.
	#자바 소스를 해석하는 자바 가상머신(JVM)은 프로그램 실행 시 자동으로
	main함수를 불러서 내부의 코드를 작동시킵니다.*/

	//영문 소문자 main입력하고 Ctril+Space: main함수 자동 생성.
	public static void main(String[] args) {
		/*콘솔창에 데이터를 출력하는 함수. println()의 단축키 sysout+Ctrl+Space
		 * 문자열 출력시에는 ""안에 문자열을 담아서 출력.*/
		System.out.println("안녕하세요");
		System.out.println("안녕히 계세요");
		System.out.println("수고하셨습니다");
		//컴파일 명령 (javac): Ctrl+s
		//실행명령(java): Ctrl+F11
		//Ctrl+/ : 한줄주석 단축키
	}

}
